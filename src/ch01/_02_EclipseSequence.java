package ch01;

public class _02_EclipseSequence {
	public static void main(String[] args) {
		/*
		* Escape Sequence 
		*
		*
		*
		*
		*
		*
		*
		*
		*
		*
		*
		*	
		*/
		
		
		System.out.println("Write\nOnce"); // 줄 바꿈
		System.out.println("사과	배	귤"); // 
		System.out.println("사과\t배\t귤"); // 탭
		System.out.println("\"사과\t배\t귤\""); // 탭 with quotation mark
	}
}
